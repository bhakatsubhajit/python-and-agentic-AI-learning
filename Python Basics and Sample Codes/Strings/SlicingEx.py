
name ="Harry0123456789"

print(name[0:2]) # returns from 0 to 2-1=1 i.e 0 to 1

print(name[2:-2]) # returns from 2 to (Len+(-ve index)) 
#i.e 2 to n-1

print(name[0:10])

#print(name[0:10:n])  skips n-1 characters

print(name[0:10:2])

print(name[:3]) # replace the first empty number with 0

print(name[4:]) # replace the first empty number with len